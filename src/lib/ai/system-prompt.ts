function buildRagContextLayer(ragContext?: string): string {
  if (!ragContext || ragContext.trim() === '') {
    return '';
  }

  return `
## 参考情報（プロ鑑定士のアドバイスより）

以下は円香さんの質問に関連するプロの鑑定データです。
回答の参考にしてください。出典を明示して引用してください。

---
${ragContext}
---`;
}

