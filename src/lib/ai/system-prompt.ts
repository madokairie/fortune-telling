/**
 * 完成済みシステムプロンプト（デフォルト: 全レイヤー結合、カテゴリなし、RAGなし）
 */
export const SYSTEM_PROMPT: string = [LAYER1_ROLE, LAYER2_MEIBAN_CORE].join(
  '\n\n'
);

/**
 * カテゴリとRAGコンテキストを含めた完全なプロンプトを構築する
 *
 * @param category - 相談カテゴリ ('mcreate' | 'jybee' | 'private' | 'general')
 * @param ragContext - RAG検索結果のテキスト（任意）
 * @returns 構築済みシステムプロンプト文字列
 */
export function buildPrompt(category?: string, ragContext?: string): string {
  const layers: string[] = [LAYER1_ROLE, LAYER2_MEIBAN_CORE];

  // Layer 3: カテゴリ別指示
  if (category && category in CATEGORY_INSTRUCTIONS) {
    const instruction = CATEGORY_INSTRUCTIONS[category];
    if (instruction) {
      layers.push(instruction);
    }
  }

  // Layer 4: RAGコンテキスト
  const ragLayer = buildRagContextLayer(ragContext);
  if (ragLayer) {
    layers.push(ragLayer);
  }

  return layers.join('\n\n');
}

